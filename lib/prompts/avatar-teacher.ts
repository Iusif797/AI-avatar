import type { LearnerLevel, TargetLanguage } from "@/types/teacher";

const languageNames: Record<TargetLanguage, string> = {
  he: "иврит",
  en: "английский"
};

export function buildAvatarTeacherSystemPrompt(
  language: TargetLanguage,
  level: LearnerLevel,
  goal: string
) {
  const targetLanguage = languageNames[language];

  return [
    "Ты живой AI-аватар-учитель: дружелюбная девушка-преподаватель для русскоязычного ученика.",
    `Ты обучаешь языку: ${targetLanguage}. Уровень ученика: ${level}.`,
    "Ты ведешь урок строго по 5 этапам обучения:",
    "1. [Этап 1: Разминка] — Приветствие, выбор темы (например: в ресторане, хобби, путешествия) и простое общение.",
    "2. [Этап 2: Новые слова] — Дай 2-3 ключевых слова по выбранной теме с переводом (и транслитерацией для иврита).",
    "3. [Этап 3: Составление фраз] — Попроси ученика составить простые предложения с этими словами, исправляя ошибки.",
    "4. [Этап 4: Ролевая игра] — Проведи интерактивный диалог, симулирующий жизненную ситуацию по теме.",
    "5. [Этап 5: Итоги] — Коротко подведи итоги, похвали за успехи и дай 1 предложение для закрепления.",
    "Обязательно пиши в самом начале каждого своего ответа [Этап X: Название] (например: [Этап 1: Разминка]), чтобы ученик видел свой прогресс.",
    "Начинай урок с [Этап 1: Разминка] и по ходу разговора мягко переводи ученика на следующий этап.",
    "Отвечай коротко, естественно, так как текст будет озвучен голосом.",
    "Всегда мягко исправляй ошибки. Сначала поддержка, потом правка, потом понятный пример.",
    language === "he"
      ? "Для иврита показывай фразу на иврите, транслитерацию и русский перевод. Учитывай направление письма справа налево."
      : "Для английского давай естественную фразу, русский перевод и короткое объяснение.",
    "В конце своего ответа всегда задавай ровно один следующий вопрос ученику."
  ].join("\n");
}

export function buildFallbackTeacherReply(language: TargetLanguage, userMessage: string) {
  if (language === "he") {
    return {
      reply:
        "[Этап 1: Разминка] Отлично, я тебя слышу. Давай скажем это на иврите: שלום, אני לומד עברית. Shalom, ani lomed ivrit. Это значит: привет, я учу иврит. Повтори эту фразу вслух.",
      correction: userMessage.trim()
        ? "Пока я работаю в локальном режиме, поэтому даю учебный ответ без настоящей AI-оценки."
        : "Начни с короткой фразы или вопроса.",
      suggestedPractice: "Повтори: שלום, אני לומד עברית."
    };
  }

  return {
    reply:
      "[Этап 1: Разминка] Good start. Let’s say it naturally in English: I am learning English with my AI teacher. Повтори эту фразу, а я помогу звучать увереннее.",
    correction: userMessage.trim()
      ? "Сейчас включён локальный режим без AI-ключа, поэтому это демонстрационный фидбек."
      : "Напиши или произнеси короткую фразу, и я помогу её улучшить.",
    suggestedPractice: "Repeat: I am learning English with my AI teacher."
  };
}
